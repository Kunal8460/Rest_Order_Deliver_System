package Entity;

import java.io.Serializable;
import java.util.Collection;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.OneToMany;
import javax.persistence.Table;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;

/**
 *
 * @author Bhatt Jaimin
 */
@Entity
@Table(name = "tax_slabs")
@NamedQueries({
    @NamedQuery(name = "TaxSlabs.findAll", query = "SELECT t FROM TaxSlabs t"),
    @NamedQuery(name = "TaxSlabs.findById", query = "SELECT t FROM TaxSlabs t WHERE t.id = :id"),
    @NamedQuery(name = "TaxSlabs.findByPercentage", query = "SELECT t FROM TaxSlabs t WHERE t.percentage = :percentage")})
public class TaxSlabs implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @Basic(optional = false)
    @NotNull
    @Size(min = 1, max = 50)
    @Column(name = "id")
    private String id;
    @Column(name = "percentage")
    private Integer percentage;
    @OneToMany(mappedBy = "taxSlabId")
    private Collection<Items> itemsCollection;

    public TaxSlabs() {
    }

    public TaxSlabs(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Integer getPercentage() {
        return percentage;
    }

    public void setPercentage(Integer percentage) {
        this.percentage = percentage;
    }

    public Collection<Items> getItemsCollection() {
        return itemsCollection;
    }

    public void setItemsCollection(Collection<Items> itemsCollection) {
        this.itemsCollection = itemsCollection;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof TaxSlabs)) {
            return false;
        }
        TaxSlabs other = (TaxSlabs) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "Entity.TaxSlabs[ id=" + id + " ]";
    }
    
}
